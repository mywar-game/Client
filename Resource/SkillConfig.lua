--隐藏
function delayTimeVisible(skeleton,timer)
	local arr = {}
	arr[1] = cc.DelayTime:create(timer)
	arr[2] = cc.CallFunc:create(function()
		skeleton:setVisible(false)
	end)
	local sq = cc.Sequence:create(arr)
	skeleton:runAction(sq)
end

function setSkeletonAnimation(resId,skeleton)
	local func = SkillAnimation[resId]
	if func then
		func(skeleton)
	end
end
